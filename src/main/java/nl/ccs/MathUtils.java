package nl.ccs;

public class MathUtils {

	public int math(int a, int b) {
		return a + b;
	}
	
	public double computeCircleArea(double radio) {
		double area = Math.PI * radio * radio;
		return area;
	}

	
	public int devide(int a, int b) {
		return a / b;
	}

	public int add(int a, int b) {
		return a + b;
	}
	
}
